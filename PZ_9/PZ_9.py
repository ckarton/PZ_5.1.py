a = {}
b = 'апельсины 45 991 63 100 12 яблоки 13 47 26 0 16'
b = b.split()
a["Продукт 1"] = b[0]
a['продажи 1'] = []
for i in b[1:5]:
    a['продажи 1'].append(int(i))
a['продукт 2'] = b[6]
a['продажи 2'] = []
for i in b[7:11]:
    a['продажи 2'].append(int(i))

max1 = max(a['продажи 1'])
max2 = max(a['продажи 2'])
print(f"Максимальные продажи апельсинов: {max1}")
print(f"Максимальные продажи яблок: {max2}")